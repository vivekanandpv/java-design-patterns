package Behavioral.CommandExercise;

public class Stock {
    public void increase() {
        System.out.println("Stock: Increase");
    }

    public void decrease() {
        System.out.println("Stock: Decrease");
    }

    public void recordLoss() {
        System.out.println("Stock: Record loss");
    }

    public void destroy() {
        System.out.println("Stock: Destroy");
    }

    public void order() {
        System.out.println("Stock: Order");
    }

    public void notifyShortage() {
        System.out.println("Stock: Notify shortage");
    }

    public void process() {
        System.out.println("Stock: Process");
    }

    public void complete() {
        System.out.println("Stock: Complete");
    }

    public void ship() {
        System.out.println("Stock: Ship");
    }
}
